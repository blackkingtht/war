using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using UnityEngine;

namespace Assets.Scripts.InGameUI
{
    class Skill
    {
        private string name;
        private Sprite img;
        private string info;
        private SkillEnum skillEnum;
        public string Name
        {
            get
            {
                return name;
            }

            set
            {
                name = value;
            }
        }

        public Sprite Img
        {
            get
            {
                return img;
            }

            set
            {
                img = value;
            }
        }

        public string Info
        {
            get
            {
                return info;
            }

            set
            {
                info = value;
            }
        }

        public SkillEnum SkillEnum
        {
            get
            {
                return skillEnum;
            }

            set
            {
                skillEnum = value;
            }
        }
    }
}
